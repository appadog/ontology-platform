function auditRef(audit_event_id: string, event_type: string): AuditEventRef {
  return {
    audit_event_id,
    category: event_type.includes("credential")
      ? "CREDENTIAL"
      : event_type.includes("policy")
        ? "POLICY"
        : event_type.includes("import") || event_type.includes("export")
          ? "IMPORT_EXPORT"
          : "ROLE",
    event_type,
    href: `/admin/audit?event=${audit_event_id}`,
  };
}

